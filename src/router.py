from src.rag import generate_with_ollama


VALID_INTENTS = {
    "log_glucose",
    "show_glucose",
    "plot_glucose",
    "log_symptom",
    "show_symptoms",
    "analyze_glucose",
    "emergency",
    "knowledge",
    "unknown",
}


ROUTER_PROMPT = """
Ты классификатор интентов для AI-ассистента по диабету.

Твоя задача — выбрать РОВНО ОДИН intent из списка:

log_glucose
show_glucose
plot_glucose
log_symptom
show_symptoms
analyze_glucose
emergency
knowledge
unknown

Определения интентов:

1. log_glucose
Пользователь хочет ЗАПИСАТЬ значение сахара в дневник.
Примеры:
- сахар 7.2
- запиши сахар 8.1 после ужина
- натощак сахар 6.4

2. show_glucose
Пользователь хочет ПОСМОТРЕТЬ историю сахара.
Примеры:
- покажи мой сахар
- покажи записи сахара
- история сахара

3. plot_glucose
Пользователь хочет увидеть ГРАФИК или динамику сахара.
Примеры:
- покажи график сахара
- построй график сахара
- динамика сахара

4. log_symptom
Пользователь хочет ЗАПИСАТЬ симптом или самочувствие в дневник.
Важно: это именно запись, а не вопрос "что делать".
Примеры:
- запиши что была слабость
- добавь симптом тошнота
- сегодня была слабость и тошнота, запиши

5. show_symptoms
Пользователь хочет ПОСМОТРЕТЬ историю симптомов.
Примеры:
- покажи симптомы
- покажи мое самочувствие
- история симптомов

6. analyze_glucose
Пользователь хочет анализ, сводку, интерпретацию дневника сахара.
Примеры:
- проанализируй мой сахар
- как у меня сахар
- анализ сахара

7. emergency
Пользователь описывает опасное или потенциально опасное состояние и спрашивает, что делать.
Примеры:
- меня трясет и тошнит что делать
- кружится голова что делать
- сахар 2.8 и трясет
- мне очень плохо что делать

8. knowledge
Любой вопрос про диабет, питание, причины скачков сахара, нормы, препараты, аналоги, сахарозаменители, алкоголь, еду, механизмы действия.
Сюда относятся ВСЕ знаниевые вопросы, если это не дневник и не emergency.
Примеры:
- от чего скачет сахар в крови
- можно ли мне есть пиццу
- можно ли пить пиво
- что такое диабет 2 типа
- что за препарат форсига
- аналог оземпика
- чем фруктоза отличается от сахарозы

9. unknown
Если совсем неясно.

Критически важные правила:
- Если пользователь ХОЧЕТ УЗНАТЬ, СПРОСИТЬ, ПОНЯТЬ, ОБЪЯСНИТЬ — это knowledge.
- Если пользователь ХОЧЕТ ЗАПИСАТЬ что-то в дневник — это log_glucose или log_symptom.
- Если пользователь ХОЧЕТ ПОСМОТРЕТЬ историю — это show_glucose или show_symptoms.
- Если есть фраза "что делать" + плохое самочувствие — это emergency.
- Не путай knowledge-вопросы с log/show symptom.
- "от чего скачет сахар в крови" -> knowledge
- "можно ли мне есть пиццу" -> knowledge
- "можно ли мне пить пиво" -> knowledge
- "что делать если меня тошнит при диабете" -> emergency
- "запиши что сегодня меня тошнило" -> log_symptom

Верни только одно слово из списка выше. Без пояснений.
""".strip()


def route_with_llm(message: str) -> str:
    prompt = f"""
{ROUTER_PROMPT}

Сообщение пользователя:
{message}
""".strip()

    try:
        label = generate_with_ollama(prompt).strip().lower()
    except Exception:
        return "unknown"

    label = label.split()[0].strip(".,:;!?")

    if label not in VALID_INTENTS:
        return "unknown"

    return label